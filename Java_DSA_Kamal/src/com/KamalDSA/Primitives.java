package com.KamalDSA;

public class Primitives {
    public static void main(String[] args) {
        //primitive -- you can't break data type even further
        int rollno = 31; // 4 bytes
        char letter= 'k';
        float marks = 98.67f; //why f?  // 4 bytes
        //f: all the decimals we have by default they are type double.
        double largeDecimalNumbers = 4587996.78965; //8 bytes
        long largeIntegerValues = 9799632631616494L; // Why L? //8 bytes
        boolean check = false;

        //NOTE: Primitive datatype has class. they are known as wrapper class.
        Integer rno = 31;


        String name="Kamalnath"; // String is not primitive

    }
}
