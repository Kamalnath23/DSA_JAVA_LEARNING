package com.KamalDSA;

public class BasicsLoop {
    public static void main(String[] args) {
        int i=1;
        while(i<=5){
            System.out.println("Value of i is "+i);
            i++;
        }

        System.out.println("for loop");
        int j=0;
        for(j=0; j<5;j++){
            System.out.println("Value of j is "+j);
        }


    }
}
